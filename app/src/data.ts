/**
 * Reorganize posts by interleaving them
 * @param {string[]} publicKeys - Array of public keys
 * @param {Record<string, any>} posts - Posts object generated by previous function
 * @returns {Array<Record<string, any>>} - Array of interleaved posts
 */
export function interleavePosts(publicKeys: string[], posts: Record<string, any>): Array<Record<string, any>> {
    const interleavedPosts: Array<Record<string, any>> = [];
    let maxPostCount = 0;

    // Find the maximum number of posts by a user
    for (const key of publicKeys) {
        const userPosts = Object.keys(posts[key]);
        maxPostCount = Math.max(maxPostCount, userPosts.length);
    }

    // Interleave posts
    for (let i = 1; i <= maxPostCount; i++) {
        for (const key of publicKeys) {
            if (posts[key][i]) {
                interleavedPosts.push(posts[key][i]);
            }
        }
    }

    return interleavedPosts;
}


